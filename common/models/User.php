    public function beforeSave($insert)
    {
        if ($this->isNewRecord) {
            self::setPassword($this->password_hash);
        } else {
            if (!self::findByPasswordHash($this->password_hash)) {
                $this->setPassword($this->password_hash);
            }
        }
        return parent::beforeSave($insert);
    }

